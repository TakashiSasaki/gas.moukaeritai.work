/**
  @constructor
  @return {Sht}
*/
function Sht(ssNumber, sheetNumber) {
  Assert.isNumberInRange(ssNumber, 0, 65);
  Assert.isNumberInRange(sheetNumber, 0, 64);
  
  if(Sht.sheets[ssNumber][sheetNumber]) {
    this.sheet = Sht.sheets[ssNumber][sheetNumber];
    Assert.sheet(this.sheet);
    return Object.seal(this);
  }//if
  
  if(!Sht.spreadsheets[ssNumber]) {
    var up = new Up(); 
    var id = up.get("ss" + ssNumber);
    Sht.spreadsheets[ssNumber] = SpreadsheetApp.openById(id);
    Assert.spreadsheet(Sht.spreadsheets[ssNumber]);
  }//if
  
  Assert.spreadsheet(Sht.spreadsheets[ssNumber]);
  Sht.sheets[ssNumber][sheetNumber] = Sht.spreadsheets[ssNumber].getSheetByName(sheetNumber.toFixed(0));
  if(Sht.sheets[ssNumber][sheetNumber]) {
    this.sheet = Sht.sheets[ssNumber][sheetNumber];
    Assert.sheet(this.sheet);
    return Object.seal(this);
  }//if

  Sht.sheets[ssNumber][sheetNumber] = Sht.spreadsheets[ssNumber].insertSheet(sheetNumber.toFixed(0));
  this.sheet = Sht.sheets[ssNumber][sheetNumber];
  Assert.sheet(this.sheet);
  return Object.seal(this);
}//Sht

/**
  @param {String} newSheetName
  @return {void}
*/
Sht.prototype.rename = function(newSheetName) {
  Assert.isString(newSheetName);
  Assert.sheet(this.sheet);
  var sheet2 = sheet.setName(newSheetName);
  Assert.sheet(sheet2);
  this.sheet = sheet2;
}//Sht.prototype.rename

/**
  @param {Array} array of array
  @return {void}
*/
Sht.prototype.append = function(values){
  Assert.sheet(this.sheet);
  Assert.isArray(values);
  Assert.isTrue(values.length > 0);
  Assert.isArray(values[0]);
  var width = values[0].length;
  var lastRow = this.sheet.getLastRow();
  var range = this.sheet.getRange(lastRow+1, 1, values.length, width);
  range.setValues(values);
}//Sht.prototype.append

